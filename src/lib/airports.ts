export function getAirportCoords(icao: string): [number, number] | null {
  return AIRPORT_COORDS[icao.toUpperCase()] || null;
}

export function getBoundingBox(
  icaos: string[],
  paddingDeg = 15.0
): { minLat: number; maxLat: number; minLon: number; maxLon: number } | null {
  const coords = icaos.map(getAirportCoords).filter(Boolean) as [number, number][];
  if (coords.length === 0) return null;

  const lons = coords.map(c => c[0]);
  const lats = coords.map(c => c[1]);

  return {
    minLat: Math.min(...lats) - paddingDeg,
    maxLat: Math.max(...lats) + paddingDeg,
    minLon: Math.min(...lons) - paddingDeg,
    maxLon: Math.max(...lons) + paddingDeg,
  };
}
